 public class rectang {
    int l,b;
    void area()
    {
          System.out.println("Area of rectangle with"+l+"and"+b+"is:"+(l*b));
          
    }
    void perimeter()
    {
          System.out.println("perimeter of rectangle with"+l+"and"+b+"is:"+(2*(l+b)));
    }
    
}
class main
        {
    public static void main(String ar[])
    {
        rectang r1=new rectang();
        r1.l=10;
        r1.b=20;
        r1.area();
        r1.perimeter();
    }
            
}
